package com.example.handy.core.bean;

public class CourseStepData {
    int stepTag;
    String stepImg;
    String stepDetail;

    public int getStepTag() {
        return stepTag;
    }

    public void setStepTag(int stepTag) {
        this.stepTag = stepTag;
    }

    public String getStepImg() {
        return stepImg;
    }

    public void setStepImg(String stepImg) {
        this.stepImg = stepImg;
    }

    public String getStepDetail() {
        return stepDetail;
    }

    public void setStepDetail(String stepDetail) {
        this.stepDetail = stepDetail;
    }
}
